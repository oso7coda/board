package board.model;

public class FileVo {
	private Integer file_num;
	private Integer file_board_seq;
	private String file_log;
	public Integer getFile_num() {
		return file_num;
	}
	public void setFile_num(Integer file_num) {
		this.file_num = file_num;
	}
	public Integer getFile_board_seq() {
		return file_board_seq;
	}
	public void setFile_board_seq(Integer file_board_seq) {
		this.file_board_seq = file_board_seq;
	}
	public String getFile_log() {
		return file_log;
	}
	public void setFile_log(String file_log) {
		this.file_log = file_log;
	}
	
	
}
